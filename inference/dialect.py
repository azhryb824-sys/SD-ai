    converted = re.sub(r"\bوبالنسبة لـ\s+ال", "وبالنسبة لل", converted)
    converted = re.sub(r"\bبالنسبة لـ\s+", "عن ", converted)
    converted = re.sub(r"\bوبالنسبة لـ\s+", "وعن ", converted)
    converted = re.sub(r"\b([وف])يجلب\b", r"\1بجيب", converted)
    converted = re.sub(r"\b([وف])يزيد\b", r"\1بزيد", converted)
    converted = re.sub(r"\b([وف])يقلل\b", r"\1بقلل", converted)
    converted = re.sub(r"\b([وف])توجد\b", r"\1في", converted)
    converted = re.sub(r"\b([وف])يوجد\b", r"\1في", converted)
    converted = re.sub(r"\bولم أتمكن من\b", "وما قدرت", converted)
